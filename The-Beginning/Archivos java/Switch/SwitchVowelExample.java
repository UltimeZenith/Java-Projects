public class SwitchVowelExample {    
	public static void main(String[] args) {    
		char ch='O';    
		switch(ch){
			case 'a':   
			System.out.println("Vocal");  
			break;

			case 'e':
			System.out.println("Vocal");  
			break;

			case 'i':
			System.out.println("Vocal");  
			break;

			case 'o':
			System.out.println("Vocal");  
			break;

			case 'u':   
			System.out.println("Vocal");  
			break;

			case 'A':   
			System.out.println("Vocal");  
			break;

			case 'E':
			System.out.println("Vocal");  
			break;

			case 'I':   
			System.out.println("Vocal");  
			break;

			case 'O':
			System.out.println("Vocal");  
			break;

			case 'U':
			System.out.println("Vocal");  
			break;

			default:   
			System.out.println("Consonante");  
		}
	}
} 