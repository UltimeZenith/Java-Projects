public class relationalOperators1{
	public static void main(String[] args){
		double valorAdquirido = 128.5;
		double valorEvaluar = 180;
		double valorAgregar = 60;
		double valorSumar = valorAdquirido + valorAgregar;
		System.out.println(valorAdquirido > valorEvaluar);
		System.out.println(valorEvaluar < valorSumar);
	}
}